package medlatec.listview.object;

public class NhanMau {

	private String sid;
	private int loai;
	private int soluong;
	private int ghichu;
	
	
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public int getLoai() {
		return loai;
	}
	public void setLoai(int loai) {
		this.loai = loai;
	}
	public int getSoluong() {
		return soluong;
	}
	public void setSoluong(int soluong) {
		this.soluong = soluong;
	}
	public int getGhichu() {
		return ghichu;
	}
	public void setGhichu(int ghichu) {
		this.ghichu = ghichu;
	}
	
}
